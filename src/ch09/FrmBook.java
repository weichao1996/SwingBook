    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(JOptionPane.showConfirmDialog(null, "你是否确定要删除该数据","确认删除",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.YES_OPTION){
            String BookID=txtBookID.getText();
            String sql="delete from Book where BookID='"+BookID+"'";
            if(DBCon.updateData(sql)){
                //刷新表格中数据
                data=DBCon.queryVectorData("select * from Book");
                showTableData();
                JOptionPane.showMessageDialog(null,"删除数据成功","系统提示",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"删除数据失败","系统提示",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

