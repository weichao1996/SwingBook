    public void showTextData2(int index){
        Vector line=(Vector)data.get(index);
        txtBookID.setText(line.get(0).toString());
        txtBookName.setText(line.get(1).toString());
        txtAuthor.setText(line.get(2).toString());
        txtPress.setText(line.get(3).toString());
        txtPressDate.setText(line.get(4).toString());
        if(line.get(5).toString().equals("在库")){
            radIn.setSelected(true);
        }else{
           radOut.setSelected(true);
       }
        lock(); //显示数据时，不允许修改
    }
    
