    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //获得输入的新数据
        String BookID=txtBookID.getText();
        String BookName=txtBookName.getText();
        String Author=txtAuthor.getText();
        String Press=txtPress.getText();
        String PressDate=txtPressDate.getText();
        String Status;
        if(radIn.isSelected()){
            Status="在库";
        }else{
            Status="借出";
        }
        String sql="";
        switch(flag){
            case 1://添加
                sql="insert into Book values('"+BookID+"','"+BookName+"','"+Author+"','"+Press+"','"+PressDate+"','"+Status+"')";
                break;
            case 2: //修改
                sql="update Book set BookName='"+BookName+"',Author='"+Author+"',Press='"+Press+"',PressDate='"+PressDate+"',Status='"+Status+"' where BookID='"+BookID+"'";
        } 
        System.out.println(sql);
        //保存到表中
        if(DBCon.updateData(sql)){
            //刷新表格中数据
            data=DBCon.queryVectorData("select * from Book");
            showTableData();
            JOptionPane.showMessageDialog(null,"保存数据成功","系统提示",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null,"保存数据失败","系统提示",JOptionPane.ERROR_MESSAGE);
        }
        lock();
    }//GEN-LAST:event_btnSaveActionPerformed

